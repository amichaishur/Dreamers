const DICTS: Record<Lang, Dict> = { he: HE, en: EN };

type Ctx = { lang: Lang; dir: Dir; t: (k: string) => string; toggle: () => void; setLang: (l: Lang) => void };
const LangContext = createContext<Ctx>({ lang: "he", dir: "rtl", t: (k) => k, toggle: () => {}, setLang: () => {} });

export function LangProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLang] = useState<Lang>("he");

  useEffect(() => {
    const saved = (localStorage.getItem("lang") as Lang) || "he";
    setLang(saved);
  }, []);

  useEffect(() => {
    const dir: Dir = lang === "he" ? "rtl" : "ltr";
    document.documentElement.lang = lang;
    document.documentElement.dir = dir;
    localStorage.setItem("lang", lang);
  }, [lang]);

  const t = useCallback((k: string) => DICTS[lang][k] ?? DICTS.he[k] ?? k, [lang]);
  const toggle = useCallback(() => setLang((l) => (l === "he" ? "en" : "he")), []);
  const dir: Dir = lang === "he" ? "rtl" : "ltr";

  return <LangContext.Provider value={{ lang, dir, t, toggle, setLang }}>{children}</LangContext.Provider>;
}

export const useLang = () => useContext(LangContext);
